def area(a, b, c):
    '''Получает на вход 3 числа - длины сторон треугольника, возвращает число - площадь треугольника'''
    return (a + b + c) / 2


def perimeter(a, b, c):
    '''Получает на вход 3 числа - длины сторон треугольника, возвращает число - периметр треугольника'''
    return a + b + c
